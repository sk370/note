public class NegativeOperation{
  public static void main(String[] args){
    System.out.println(10 / 3);
    System.out.println(-10 / 3);
    System.out.println(10 / -3);
    System.out.println(-10 / -3);

    System.out.println("===========");

    System.out.println(10 % 3);
    System.out.println(-10 % 3);
    System.out.println(10 % -3);
    System.out.println(-10 % -3);

    System.out.println("===========");

    System.out.println(8>>3);
    System.out.println(-8>>3);
    System.out.println(-8>>>3);



  }
}