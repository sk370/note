public class DefineVariables02{
  public static void main(String[] args){
    int nums = 100;
    System.out.println("班级里有" + nums + "个学生");

    float price = 55.55f;
    System.out.println("某商品价格" + price + "元");

    long age = 460000000L;
    System.out.println("地球已经诞生" + age + "年");

    boolean flag = false;
    System.out.println("\"5是偶数\"这句话是" + flag + "的");
  }
}